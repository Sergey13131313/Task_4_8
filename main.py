# Преобразовать IP-адрес в переменной ip в двоичный формат
# и вывести на стандартный поток вывода вывод столбцами:
# Пример вывода для адреса 10.1.1.1:
# 10        1         1         1
# 00001010  00000001  00000001  00000001

ip = "192.168.3.1"
ip = ip.split('.')
for x in ip:
    print('%10s' % x, end='')

ip1 = []
for x in ip:
    ip1.append(bin(int(x)))




print()
for x in ip1:
    print('%10s' % x[2:], end='')

